            <li><span style='font-family:"Times New Roman";color:#0E101A;'>In a small saucepan, over low heat, melt the butter and chocolate together.&nbsp;</span></li>
            <li><span style='font-family:"Times New Roman";color:#0E101A;'>Remove the pan from the heat and stir in the sugar, cocoa, and vanilla.&nbsp;</span></li>
            <li><span style='font-family:"Times New Roman";color:#0E101A;'>Whisk together the eggs, flour, baking powder, and salt until blended. If using, stir in the walnuts.&nbsp;</span></li>
            <li><span style='font-family:"Times New Roman";color:#0E101A;'>Pour the batter into an 8&quot; &times; 8&quot; baking pan that has been sprayed with nonstick cooking spray.&nbsp;</span></li>
            <li><span style='font-family:"Times New Roman";color:#0E101A;'>Pour the batter into an equal layer on the baking sheet.</span></li>
