            <li><span style="font-family:Calibri;">In a separate dish, whisk together buttermilk, eggs, and melted butter.&nbsp;</span></li>
            <li><span style="font-family:Calibri;">Stir just until the buttermilk mixture is absorbed into the flour mixture.&nbsp;</span></li>
            <li><span style="font-family:Calibri;">Preheat a waffle iron with a circular shape.&nbsp;</span></li>
            <li><span style="font-family:Calibri;">Spoon a heaping 1/2 cup batter onto the waffle pan and cook for 5 to 6 minutes, or until light golden brown.</span></li>
            <li><span style="font-family:Calibri;">Allow cooling on a wire rack. Once cold, spread ice cream equally on half of the waffles.&nbsp;</span></li>
