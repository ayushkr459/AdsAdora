        <img class="mt-3" src="../assets/images/bewitch7-min.png" alt="food" style="width:100%; object-fit:cover;">
        <p style='margin-right:0cm;margin-left:0cm;font-size:16px;font-family:"Calibri",sans-serif;margin:0cm;'>&nbsp;</p>
