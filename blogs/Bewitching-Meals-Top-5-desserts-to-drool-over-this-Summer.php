            <li><span style="font-family:Calibri;">Combine the ricotta, cream cheese, 3/4 cup sugar, and lemon peel and swirl it in the food processor until smooth.</span></li>
            <li><span style="font-family:Calibri;">Add the eggs and press the pulse button on your processor just long enough to mix it well. Pour the egg and cheese mixture over the pan&apos;s biscotti crust.&nbsp;</span></li>
            <li><span style="font-family:Calibri;">In a baking dish, place the springform pan. Fill the dish with enough boiling water to reach halfway up the sides of the pan.&nbsp;</span></li>
            <li><span style="font-family:Calibri;">Bake for 1 hour, or until the cheesecake is brown and the center portion begins to shake while gently moving the pan.</span></li>
            <li><span style="font-family:Calibri;">Allow it to cool for an hour on the counter before refrigerating for at least 4 hours.&nbsp;</span></li>
            <li><span style="font-family:Calibri;">In a saucepan, mix the blueberries and lemon juice with two tablespoons of sugar when the cheesecake is ready to be sliced.&nbsp;</span></li>
            <li><span style="font-family:Calibri;">Simmer for 5-7 minutes, or until blueberries start to burst and the liquid thickens.&nbsp;</span></li>
