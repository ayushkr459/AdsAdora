            <li>In a large mixing bowl, beat softened butter and sugar with an electric whisker on medium speed for 1 minute or until frothy and scraping down the sides of the bowl.&nbsp;</li>
            <li>In a separate bowl, whisk together the egg and vanilla extract until creamy. Stir the flour until thoroughly combined and stiff.&nbsp;</li>
            <li>Combine the chocolate chips and almonds in a mixing bowl.</li>
            <li>Drop some dough with a rounded tablespoonfuls, from at least 2 inches apart, onto prepared cookie sheets.&nbsp;</li>
            <li>Preheat oven at 350&deg;F and bake for 8 to 10 minutes, or until golden brown (centers will be soft). Allow cooling for 2 minutes before transferring to a cooling rack. Allow 30 minutes for cooling.&nbsp;</li>
        </ol>
